#include<stdio.h>
void subsequence(char * seq,char * str,int len,int ind)
{
    if(ind==len)
    {
        return;
    }
    for(int i = ind; i < len; i++) 
    {
        seq[ind]=str[i];
        printf("%s\n",seq);
        subsequence(seq,str,len,ind+1);
        seq[ind]='\0';
    }
}
int main()
{
    int len;
    char str[1000],seq[1000]="\0";
    scanf("%s%n",str,&len);
    subsequence(seq,str,len,0);
}
